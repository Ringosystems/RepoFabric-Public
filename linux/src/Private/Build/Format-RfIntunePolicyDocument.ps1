function Format-RfIntunePolicyDocument {
    <#
    .SYNOPSIS
        Builds a Microsoft Intune Settings Catalog policy document for the
        DesktopAppInstaller CSP, ready to be POSTed to
        /beta/deviceManagement/configurationPolicies (or imported via the
        Settings Catalog UI).

    .DESCRIPTION
        Output is the JSON body Intune expects. Operators download this
        from the admin UI and import it under their own Graph credentials.
        See the linux/admin/static/intune-deploy.html wizard for the
        operator-facing surface.

        The two source-related policies (EnableAdditionalSources and
        EnableAllowedSources) require an XML payload describing the WinGet
        REST source. We generate that via Format-RfAdditionalSourcesXml.

    .PARAMETER PolicyName
        Display name as it will appear in Intune.

    .PARAMETER PolicyDescription
        Optional description.

    .PARAMETER Settings
        Hashtable of policy short-name to one of 'enabled' | 'disabled' |
        'not_configured'. Unknown policies are ignored. Policies not in the
        hashtable are not emitted (Intune treats absence as "Not Configured").

    .PARAMETER SourceName
        WinGet source name used inside EnableAdditionalSources /
        EnableAllowedSources payloads.

    .PARAMETER SourceUrl
        REST source URL.

    .PARAMETER SourceIdentifier
        Internal identifier embedded in the <Sources> XML.

    .PARAMETER SourceAutoUpdateMinutes
        Integer value for the SourceAutoUpdateInterval policy. Ignored if
        Settings.SourceAutoUpdateInterval is not 'enabled'.

    .OUTPUTS
        Hashtable { Json = <Settings Catalog JSON string>,
                    OmaUri = <array of { Name, Path, DataType, Value }> }.
        Caller decides what to send to the wire.
    #>
    [CmdletBinding()]
    [OutputType([hashtable])]
    param(
        [Parameter(Mandatory)][string]$PolicyName,
        [string]$PolicyDescription = 'Generated by RepoFabric (RingoSystems Heavy Industries). Locks the DesktopAppInstaller surface and pins clients to the private REST source.',
        [Parameter(Mandatory)][hashtable]$Settings,
        [Parameter(Mandatory)][string]$SourceUrl,
        [string]$SourceName              = 'repofabric',
        [string]$SourceIdentifier        = 'RfPrivate',
        [int]$SourceAutoUpdateMinutes    = 60
    )

    # ----- Policy catalog -------------------------------------------------
    # name (lowercase short name used by the operator-facing form / our
    # Settings hashtable keys) -> @{
    #   Kind       = 'choice' | 'simple_string' | 'simple_integer'
    #   OmaPath    = the OMA-URI string
    #   SettingId  = the Settings Catalog settingDefinitionId
    # }
    # Settings Catalog setting-id pattern is
    # device_vendor_msft_policy_config_desktopappinstaller_<short_lowercase>.
    $catalog = @{
        'EnableAppInstaller'                              = @{ Kind='choice'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableAppInstaller' }
        'EnableSettings'                                  = @{ Kind='choice'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableSettings' }
        'EnableExperimentalFeatures'                      = @{ Kind='choice'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableExperimentalFeatures' }
        'EnableLocalManifestFiles'                        = @{ Kind='choice'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableLocalManifestFiles' }
        'EnableHashOverride'                              = @{ Kind='choice'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableHashOverride' }
        'EnableLocalArchiveMalwareScanOverride'           = @{ Kind='choice'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableLocalArchiveMalwareScanOverride' }
        'EnableMSAppInstallerProtocol'                    = @{ Kind='choice'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableMSAppInstallerProtocol' }
        'EnableDefaultSource'                             = @{ Kind='choice'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableDefaultSource' }
        'EnableMicrosoftStoreSource'                      = @{ Kind='choice'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableMicrosoftStoreSource' }
        'EnableBypassCertificatePinningForMicrosoftStore' = @{ Kind='choice'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableBypassCertificatePinningForMicrosoftStore' }
        'EnableWindowsPackageManagerCommandLineInterfaces'= @{ Kind='choice'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableWindowsPackageManagerCommandLineInterfaces' }
        'EnableWindowsPackageManagerConfiguration'        = @{ Kind='choice'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableWindowsPackageManagerConfiguration' }
        'EnableAdditionalSources'                         = @{ Kind='simple_string'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableAdditionalSources' }
        'EnableAllowedSources'                            = @{ Kind='simple_string'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/EnableAllowedSources' }
        'SourceAutoUpdateInterval'                        = @{ Kind='simple_integer'; OmaPath='./Device/Vendor/MSFT/Policy/Config/DesktopAppInstaller/SourceAutoUpdateInterval' }
    }
    foreach ($k in @($catalog.Keys)) {
        $catalog[$k].SettingId = 'device_vendor_msft_policy_config_desktopappinstaller_' + $k.ToLowerInvariant()
    }

    # Pre-compute the source XML once; both source-related policies reuse it.
    $sourcesXml = $null
    if ($Settings.ContainsKey('EnableAdditionalSources') -or $Settings.ContainsKey('EnableAllowedSources')) {
        $sourcesXml = Format-RfAdditionalSourcesXml -Arg $SourceUrl -Name $SourceName -Identifier $SourceIdentifier
    }

    # ----- Build the settings[] array and the parallel OMA-URI table -----
    $settingsArr = [System.Collections.Generic.List[object]]::new()
    $omaUri      = [System.Collections.Generic.List[object]]::new()

    foreach ($shortName in $catalog.Keys | Sort-Object) {
        $cfg = $catalog[$shortName]
        if (-not $Settings.ContainsKey($shortName)) { continue }
        $state = [string]$Settings[$shortName]
        if ($state -eq 'not_configured' -or -not $state) { continue }

        switch ($cfg.Kind) {
            'choice' {
                # ADMX-backed enable/disable. Choice value follows
                # <settingId>_0 (disabled) / _1 (enabled).
                $choiceSuffix = if ($state -eq 'enabled') { '1' } else { '0' }
                $choiceValue  = $cfg.SettingId + '_' + $choiceSuffix
                $settingsArr.Add(@{
                    '@odata.type' = '#microsoft.graph.deviceManagementConfigurationSetting'
                    settingInstance = @{
                        '@odata.type'         = '#microsoft.graph.deviceManagementConfigurationChoiceSettingInstance'
                        settingDefinitionId   = $cfg.SettingId
                        choiceSettingValue    = @{
                            '@odata.type' = '#microsoft.graph.deviceManagementConfigurationChoiceSettingValue'
                            value         = $choiceValue
                            children      = @()
                        }
                    }
                }) | Out-Null
                $omaUri.Add(@{
                    Policy   = $shortName
                    Path     = $cfg.OmaPath
                    DataType = 'String'
                    Value    = if ($state -eq 'enabled') { '<enabled/>' } else { '<disabled/>' }
                }) | Out-Null
            }
            'simple_string' {
                # Source-related policies; the value is the raw XML.
                $valueText = $sourcesXml
                $settingsArr.Add(@{
                    '@odata.type' = '#microsoft.graph.deviceManagementConfigurationSetting'
                    settingInstance = @{
                        '@odata.type'         = '#microsoft.graph.deviceManagementConfigurationSimpleSettingInstance'
                        settingDefinitionId   = $cfg.SettingId
                        simpleSettingValue    = @{
                            '@odata.type' = '#microsoft.graph.deviceManagementConfigurationStringSettingValue'
                            value         = $valueText
                        }
                    }
                }) | Out-Null
                $omaUri.Add(@{
                    Policy   = $shortName
                    Path     = $cfg.OmaPath
                    DataType = 'String'
                    Value    = $valueText
                }) | Out-Null
            }
            'simple_integer' {
                $intValue = [int]$SourceAutoUpdateMinutes
                $settingsArr.Add(@{
                    '@odata.type' = '#microsoft.graph.deviceManagementConfigurationSetting'
                    settingInstance = @{
                        '@odata.type'         = '#microsoft.graph.deviceManagementConfigurationSimpleSettingInstance'
                        settingDefinitionId   = $cfg.SettingId
                        simpleSettingValue    = @{
                            '@odata.type' = '#microsoft.graph.deviceManagementConfigurationIntegerSettingValue'
                            value         = $intValue
                        }
                    }
                }) | Out-Null
                $omaUri.Add(@{
                    Policy   = $shortName
                    Path     = $cfg.OmaPath
                    DataType = 'Integer'
                    Value    = $intValue
                }) | Out-Null
            }
        }
    }

    # ----- Wrap in the Settings Catalog policy envelope -------------------
    $policy = [ordered]@{
        name              = $PolicyName
        description       = $PolicyDescription
        platforms         = 'windows10'
        technologies      = 'mdm'
        templateReference = @{ templateId = '' }
        settings          = @($settingsArr)
    }

    return @{
        Json    = (ConvertTo-Json -InputObject $policy -Depth 20)
        OmaUri  = @($omaUri)
        Summary = @{
            PolicyName     = $PolicyName
            SettingsCount  = $settingsArr.Count
            SourceUrl      = $SourceUrl
            SourceName     = $SourceName
        }
    }
}
